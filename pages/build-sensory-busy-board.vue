<p>A busy board, or sensory board, is a great idea to keep your child entertained whilst improving their motor skills.</p>

<img src="/sensory-busy-board.jpg" alt="sensory busy board" style="width:100%;max-width:800px;">

<p>Buying one online seems to cost about £150 upwards but it's common to build these yourself using spare parts you find around the house.  It's also pretty easy and a fun project to do with other parents</p>

<p>My big tip is to collaborate with some other parents as when we visited B&Q we found many items, like springy door stops, were sold in packs of 3 - so you could share the cost with two other parents if you only need 1.</p>

<p>When designing your own busy board it's important to know how large you want it, where you want to place it and whether it will be portable and hang off something or be fixed in place.  Many items on our wooden sensory board aren't water proof so it's useful to have the option to bring it inside of it starts raining.</p>

<p>We went to B&Q and struggled to find the right size wood we wanted until I found two wooden garden tiles - so in the end we've ended up with two wooden sensory boards. This allows us to hand them at opposite ends of a play area </p>

<p>The gaps in the wood make it very easy to hang the boards either on the garden decking or inside on the radiators.</p>

<p>In the busy board we have a doorbell, a key guard, a solar powered calculator, door stops, suitcase wheels and various locks.  You might also want to screw in their name using metal letters.</p>

<p>The most important thing to remember is to watch your child as they play with the board to ensure they're safe.</p>

<p>Overall we managed to keep the cost under £60, which is a big saving versus buying one premade.</p>

